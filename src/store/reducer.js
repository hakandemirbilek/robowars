const reducer = ( state = initialState, action ) => {
    switch (action.type) {
        case 'SET_POSITION' :
        return {
            ...state,
            robots : {
                ...state.robots,
                [action.id] : {
                    ...state.robots[action.id],
                    X: action.X,
                    Y: action.Y,
                    incrementX : action.incrementX,
                    incrementY : action.incrementY,
                    speed : action.speed,
                }
            }
        }
        case 'SET_GAME_OVER' :
        return {
            ...state,
            gameOver : true,
        }
    }
    return state;
}


export default reducer;
